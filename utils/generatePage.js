function specialField(employee) {
  switch (employee.getRole()) {
    case "Manager":
      return `Office: ${employee.getOfficeNumber()}`;
    case "Engineer":
      return `GitHub: ${employee.getGithub()}`;
    case "Intern":
      return `School: ${employee.getSchool()}`;
  }
}

